    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        String profissao = txtProfissao.getText();
        int usuarioID = Integer.parseInt(txtusuario.getText());
        String habilidades = txtHabilidade.getText();

        Freelancer f = new Freelancer();
        f.setUsuarioID(usuarioID);
        f.setProfissao(profissao);
        f.setHabilidades(habilidades);
        
        FreelancerDAO freelancerDAO = new FreelancerDAO();
        freelancerDAO.inserir(f);
        
        txtusuario.setText("");
        txtProfissao.setText("");
        txtHabilidade.setText("");
    }//GEN-LAST:event_btnCriarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int UsuarioID = Integer.parseInt(txtusuario.getText());
        String profissao = txtProfissao.getText();
        String habilidades = txtHabilidade.getText();
        Freelancer f = new Freelancer();
        f.setUsuarioID(UsuarioID);
        f.setProfissao(profissao);
        f.setHabilidades(habilidades);

        FreelancerDAO freelancerDAO = new FreelancerDAO();
        freelancerDAO.alterar(f);

        txtusuario.setText("");
        txtProfissao.setText("");
        txtHabilidade.setText("");
    }//GEN-LAST:event_btnAtualizarActionPerformed

