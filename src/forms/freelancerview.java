    private void btnEncontrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncontrarActionPerformed
        int idPesquisa = Integer.parseInt(txtIDPesquisar.getText());
        FreelancerDAO freelancerDAO = new FreelancerDAO();
        Freelancer f = freelancerDAO.getFreelancer(idPesquisa);
        if (f == null) {
            JOptionPane.showMessageDialog(this, "Produto n√£o encontrado");
        } else {
            txtusuario.setText(String.valueOf(f.getUsuarioID()));
            txtProfissao.setText(f.getProfissao());
            txtHabilidade.setText(f.getHabilidades());
        }
    }//GEN-LAST:event_btnEncontrarActionPerformed

